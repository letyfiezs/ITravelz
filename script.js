async function handleBookingLogin(e) {
  e.preventDefault();
  
  const email = document.getElementById('booking-login-email').value;
  const password = document.getElementById('booking-login-password').value;
  const errorDiv = document.getElementById('booking-login-error');
  
  try {
    const response = await fetch('/api/auth/login', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({ email, password }),
    });

    const data = await response.json();
    console.log('📱 LOGIN RESPONSE:', data);

    if (response.ok && data.success && data.token && data.data) {
      // Store user data
      const userToStore = {
        id: data.data.id,
        email: data.data.email,
        name: data.data.name,
        token: data.token
      };
      
      console.log('💾 Saving to localStorage:', userToStore);
      localStorage.setItem('userLoggedIn', JSON.stringify(userToStore));
      
      // Verify it was saved
      const savedData = JSON.parse(localStorage.getItem('userLoggedIn'));
      console.log('✓ Verified saved data:', savedData);
      console.log('✓ Login successful!');
      
      // Close modal
      closeBookingLoginModal();
      
      // Proceed to booking if package is stored
      if (window.pendingBookingPackage) {
        const pkg = window.pendingBookingPackage;
        sessionStorage.setItem('selectedPackage', JSON.stringify({ 
          _id: pkg._id,
          name: pkg.name, 
          price: pkg.price, 
          duration: pkg.duration 
        }));
        setTimeout(() => {
          window.location.href = 'booking-form.html';
        }, 500);
      }
    } else {
      errorDiv.style.display = 'block';
      errorDiv.textContent = data.message || 'Login failed. Please check your credentials.';
    }
  } catch (error) {
    console.error('Login error:', error);
    errorDiv.style.display = 'block';
    errorDiv.textContent = 'An error occurred. Please try again.';
  }
}

