function setupContactForm() {
  const contactForm = document.getElementById('contact-form');
  if (!contactForm) return;

  contactForm.addEventListener('submit', async (e) => {
    e.preventDefault();

    const name = document.getElementById('contact-name').value.trim();
    const email = document.getElementById('contact-email').value.trim();
    const inquiryType = document.getElementById('contact-type').value;
    const subject = document.getElementById('contact-subject').value.trim();
    const message = document.getElementById('contact-message').value.trim();

    // Validate fields
    if (!name || !email || !subject || !message) {
      showContactMessage('Please fill in all fields', 'error');
      return;
    }

    // Validate email format
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      showContactMessage('Please enter a valid email address', 'error');
      return;
    }

    try {
      const submitBtn = document.getElementById('contact-submit-btn');
      const originalText = submitBtn.textContent;
      submitBtn.disabled = true;
      submitBtn.textContent = 'Sending...';

      const response = await fetch('/api/contact', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({
          name,
          email,
          inquiryType,
          subject,
          message
        })
      });

      const data = await response.json();

      if (response.ok) {
        showContactMessage(
          'Thank you! Your message has been received. We will respond shortly.',
          'success'
        );
        contactForm.reset();
      } else {
        showContactMessage(
          data.message || 'Error submitting form. Please try again.',
          'error'
        );
      }
    } catch (error) {
      console.error('Contact form error:', error);
      showContactMessage(
        'Error submitting form. Please try again later.',
        'error'
      );
    } finally {
      submitBtn.disabled = false;
      submitBtn.textContent = originalText;
    }
  });
}

function showContactMessage(message, type) {
  const messageBox = document.getElementById('contact-message-box');
  if (!messageBox) return;

  messageBox.textContent = message;
  messageBox.className = type === 'success' 
    ? 'success-message' 
    : 'error-message';
  messageBox.style.display = 'block';

  // Auto-hide success message after 5 seconds
  if (type === 'success') {
    setTimeout(() => {
      messageBox.style.display = 'none';
    }, 5000);
  }
}

// Initialize contact form when DOM is ready
document.addEventListener('DOMContentLoaded', function() {
  setupContactForm();
});


