function attachBookButtonListeners() {
  console.log('Attaching book button listeners...');
  document.querySelectorAll('.book-button').forEach(button => {
    button.addEventListener('click', function (e) {
      e.preventDefault();
      const id = this.getAttribute('data-id');
      const name = this.getAttribute('data-name');
      const price = this.getAttribute('data-price');
      const duration = this.getAttribute('data-duration');
      const bookingLimitPerSlot = this.getAttribute('data-booking-limit');
      console.log('Dynamic book button clicked:', { id, name, price, duration, bookingLimitPerSlot });
      
      // Check if user is logged in
      if (!isUserLoggedIn()) {
        // Store package info to use after login
        window.pendingBookingPackage = { _id: id, name, price, duration, bookingLimitPerSlot };
        showBookingLoginModal();
      } else {
        // User is logged in, proceed to booking
        sessionStorage.setItem('selectedPackage', JSON.stringify({ _id: id, name, price, duration, bookingLimitPerSlot }));
        console.log('Stored in sessionStorage:', sessionStorage.getItem('selectedPackage'));
        window.location.href = 'booking-form.html';
      }
    });
  });
  console.log('Listeners attached to', document.querySelectorAll('.book-button').length, 'buttons');
}

